# String slicing example
text = "Python Programming"

# Various slicing examples
print("Original String:", text)
print("From index 0 to 5:", text[0:6])       # 'Python'
print("From index 7 onwards:", text[7:])     # 'Programming'
print("Up to index 6:", text[:6])            # 'Python'
print("Substring between 0 and 10:", text[0:10])  
print("Last 5 characters:", text[-5:])       # 'mming'
print("Every 2nd character:", text[::2])     # 'Pto rgamn'
print("Reverse string:", text[::-1])         # Reverse
