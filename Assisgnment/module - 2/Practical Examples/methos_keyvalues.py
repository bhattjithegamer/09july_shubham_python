# Creating a dictionary
dict = {
    "name": "shubham",
    "age": 22,
    "city": "palani",
    "email": "s@example.com"
}

print("Original Dictionary:", dict)

# Getting keys
keys = dict.keys()
print("Keys:", keys)

# Getting values
values = dict.values()
print("Values:", values)

# Converting to list (optional)
print("Keys as List:", list(keys))
print("Values as List:", list(values))
