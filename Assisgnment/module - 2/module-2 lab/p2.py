# List create
my_list = [10, "Hello", 3.14, True, [1, 2, 3]]

# Positive indexing
print("Element at index 0:", my_list[0])   # 10
print("Element at index 1:", my_list[1])   # Hello

# Negative indexing
print("Element at index -1:", my_list[-1]) # [1, 2, 3]
print("Element at index -2:", my_list[-2]) # True

